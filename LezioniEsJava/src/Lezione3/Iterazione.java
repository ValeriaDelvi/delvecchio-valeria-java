package Lezione3;

public class Iterazione {
    public static void main(String[] args) {
        /*
         * while (condizone bool){
         *
         *  }
         *

        int i=0;
        while (i < 10) {
            System.out.println(i);
            i++;
            /*
             * for (inizializzazione, condizione,aggiornamento){
             *istruzioni
             *  }

        }
        */


        for (int j = 0; j < 10; j++) {
            System.out.println(j);
        }
    }
}



